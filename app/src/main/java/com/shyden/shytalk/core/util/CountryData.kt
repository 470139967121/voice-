fun flagEmojiForCode(code: String): String {
    if (code.length != 2) return ""
    val first = Character.toChars(0x1F1E6 + (code[0].uppercaseChar() - 'A'))
    val second = Character.toChars(0x1F1E6 + (code[1].uppercaseChar() - 'A'))
    return String(first) + String(second)
}

fun countryNameForCode(code: String): String? {
    return countries.find { it.code == code.uppercase() }?.name
}

